package org.cryptotrader.universal.library.entity;

import jakarta.persistence.*;
import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
@MappedSuperclass
public class AutoGeneratedIdentifiableEntity extends Identifiable<Long> {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    protected Long id;

    public AutoGeneratedIdentifiableEntity() {
        this.id = null;
    }

    public AutoGeneratedIdentifiableEntity(Long id) {
        this.id = id;
    }
}
