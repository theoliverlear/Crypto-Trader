package org.cryptotrader.console.library.entity;

import jakarta.annotation.PostConstruct;
import jakarta.persistence.*;
import lombok.NoArgsConstructor;
import org.cryptotrader.api.library.entity.user.ProductUser;
import org.cryptotrader.universal.library.entity.AutoGeneratedIdentifiableEntity;
import org.cryptotrader.universal.library.model.annotation.Loggable;

import java.time.LocalDateTime;

@Entity
@NoArgsConstructor
@Table(name = "console_command_executions")
public class ConsoleCommandExecution extends AutoGeneratedIdentifiableEntity {

    @Loggable
    @Column(name = "input")
    private String input;

    @Loggable
    @Column(name = "resolved_command")
    private String resolvedCommandName;

    @Column(name = "last_updated")
    private LocalDateTime lastUpdated;

    @ManyToOne
    @JoinColumn(name = "user_id")
    private ProductUser user;

    @Loggable
    @Column(name = "successful")
    private boolean successful;

    @Column(name = "error_message")
    private String errorMessage;

    public ConsoleCommandExecution(String input,
                                   String resolvedCommandName,
                                   boolean successful) {
        this.input = input;
        this.resolvedCommandName = resolvedCommandName;
        this.successful = successful;
        this.lastUpdated = LocalDateTime.now();
        this.user = null;
        this.errorMessage = null;
    }

    public ConsoleCommandExecution(String input,
                                   String resolvedCommandName,
                                   boolean successful,
                                   String errorMessage) {
        this.input = input;
        this.resolvedCommandName = resolvedCommandName;
        this.successful = successful;
        this.lastUpdated = LocalDateTime.now();
        this.user = null;
        this.errorMessage = errorMessage;
    }

    public ConsoleCommandExecution(String input,
                                   String resolvedCommandName,
                                   boolean successful,
                                   ProductUser user) {
        this.input = input;
        this.resolvedCommandName = resolvedCommandName;
        this.successful = successful;
        this.lastUpdated = LocalDateTime.now();
        this.user = user;
        this.errorMessage = null;
    }

    public ConsoleCommandExecution(String input,
                                   String resolvedCommandName,
                                   boolean successful,
                                   ProductUser user,
                                   String errorMessage) {
        this.input = input;
        this.resolvedCommandName = resolvedCommandName;
        this.successful = successful;
        this.lastUpdated = LocalDateTime.now();
        this.user = user;
        this.errorMessage = errorMessage;
    }

    @PostConstruct
    public void init() {
        if (this.resolvedCommandName != null) {
            this.resolvedCommandName = this.resolvedCommandName.trim().toUpperCase();
        }
    }
}