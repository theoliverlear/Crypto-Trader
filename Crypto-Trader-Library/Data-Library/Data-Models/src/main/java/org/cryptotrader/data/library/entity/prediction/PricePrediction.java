package org.cryptotrader.data.library.entity.prediction;

import jakarta.persistence.*;
import org.cryptotrader.universal.library.entity.AutoGeneratedIdentifiableEntity;
import org.cryptotrader.data.library.entity.prediction.builder.PricePredictionBuilder;
import org.cryptotrader.universal.library.model.annotation.Loggable;

import java.time.LocalDateTime;

@Entity
@Table(name = "predictions")
public class PricePrediction extends AutoGeneratedIdentifiableEntity {
    @Loggable
    @Column(name = "currency_code")
    private String currencyCode;

    @Loggable
    @Column(name = "currency_name")
    private String currencyName;

    @Loggable
    @Column(name = "predicted_price")
    private double predictedPrice;

    @Loggable
    @Column(name = "actual_price")
    private double actualPrice;

    @Loggable
    @Column(name = "price_difference")
    private double priceDifference;

    @Loggable
    @Column(name = "percent_difference")
    private double percentDifference;

    @Loggable
    @Column(name = "model_type")
    @Enumerated(EnumType.STRING)
    private ModelType modelType;

    @Loggable
    @Column(name = "num_rows")
    private int numRows;

    @Column(name = "last_updated")
    private LocalDateTime lastUpdated;

    public PricePrediction() {
        this.currencyCode = "";
        this.currencyName = "";
        this.predictedPrice = 0.0;
        this.actualPrice = 0.0;
        this.priceDifference = 0.0;
        this.percentDifference = 0.0;
        this.modelType = ModelType.LSTM;
        this.numRows = 0;
        this.lastUpdated = LocalDateTime.now();
    }
    public PricePrediction(String currencyCode,
                           String currencyName,
                           double predictedPrice,
                           double actualPrice,
                           double priceDifference,
                           double percentDifference,
                           ModelType modelType,
                           int numRows,
                           LocalDateTime lastUpdated) {
        this.currencyCode = currencyCode;
        this.currencyName = currencyName;
        this.predictedPrice = predictedPrice;
        this.actualPrice = actualPrice;
        this.priceDifference = priceDifference;
        this.percentDifference = percentDifference;
        this.modelType = modelType;
        this.numRows = numRows;
        this.lastUpdated = lastUpdated;
    }
    public static PricePredictionBuilder builder() {
        return new PricePredictionBuilder();
    }
}
