package org.cryptotrader.data.library.entity.training;

import jakarta.persistence.*;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import org.cryptotrader.universal.library.entity.AutoGeneratedIdentifiableEntity;
import org.cryptotrader.data.library.entity.currency.Currency;
import org.cryptotrader.data.library.entity.prediction.ModelType;
import org.cryptotrader.data.library.entity.prediction.PricePrediction;
import org.cryptotrader.data.library.entity.prediction.PricePredictionLookup;
import org.cryptotrader.data.library.entity.training.builder.TrainingSessionBuilder;
import org.cryptotrader.data.library.entity.training.specs.QueryLoad;
import org.cryptotrader.data.library.entity.training.specs.TrainingDevice;
import org.cryptotrader.data.library.entity.training.specs.TrainingQueryType;
import org.cryptotrader.universal.library.model.annotation.Loggable;

import java.time.LocalDateTime;

@Getter
@Setter
@Entity
@Table(name = "training_sessions")
@NoArgsConstructor
public class TrainingSession extends AutoGeneratedIdentifiableEntity {
    @ManyToOne(fetch = FetchType.LAZY, optional = false)
    @JoinColumn(name = "currency_code", nullable = false)
    private Currency currency;

    @OneToOne(fetch = FetchType.LAZY, optional = false, cascade = {CascadeType.MERGE, CascadeType.REFRESH})
    @JoinColumn(name = "prediction_id", nullable = false)
    private PricePrediction prediction;

    @Loggable
    @Column(name = "num_rows", nullable = false)
    private int numRows;

    @Loggable
    @Column(name = "epochs_trained", nullable = false)
    private int epochsTrained;

    @Loggable
    @Column(name = "max_epochs", nullable = false)
    private int maxEpochs;

    @Loggable
    @Column(name = "starting_loss", nullable = false)
    private double startingLoss;

    @Loggable
    @Column(name = "final_loss", nullable = false)
    private double finalLoss;

    @Loggable
    @Enumerated(EnumType.STRING)
    @Column(name = "model_type", length = 50, nullable = false)
    private ModelType modelType;

    @Loggable
    @Enumerated(EnumType.STRING)
    @Column(name = "query_type", length = 50, nullable = false)
    private TrainingQueryType queryType;

    @Loggable
    @Column(name = "training_start_time", nullable = false)
    private LocalDateTime trainingStartTime;

    @Loggable
    @Column(name = "training_end_time")
    private LocalDateTime trainingEndTime;

    @Loggable
    @Column(name = "query_start_time")
    private LocalDateTime queryStartTime;

    @Loggable
    @Column(name = "query_end_time")
    private LocalDateTime queryEndTime;

    @Loggable
    @Column(name = "sequence_length", nullable = false)
    private int sequenceLength;

    @Loggable
    @Column(name = "batch_size", nullable = false)
    private int batchSize;

    @Loggable
    @Column(name = "dimension_width", nullable = false)
    private int dimensionWidth;

    @Loggable
    @Enumerated(EnumType.STRING)
    @Column(name = "query_load", length = 50, nullable = false)
    private QueryLoad queryLoad;

    @Loggable
    @Column(name = "query_batch_size")
    private Integer queryBatchSize;

    @Loggable
    @Enumerated(EnumType.STRING)
    @Column(name = "training_device", length = 50, nullable = false)
    private TrainingDevice trainingDevice;

    @Loggable
    @Column(name = "short_sequence_length")
    private Integer shortSequenceLength;

    @Loggable
    @Column(name = "medium_sequence_length")
    private Integer mediumSequenceLength;

    @Loggable
    @Column(name = "long_sequence_length")
    private Integer longSequenceLength;

    public TrainingSession(Currency currency,
                           PricePrediction prediction,
                           int numRows,
                           int epochsTrained,
                           int maxEpochs,
                           double startingLoss,
                           double finalLoss,
                           ModelType modelType,
                           TrainingQueryType queryType,
                           LocalDateTime trainingStartTime,
                           LocalDateTime trainingEndTime,
                           LocalDateTime queryStartTime,
                           LocalDateTime queryEndTime,
                           int sequenceLength,
                           int batchSize,
                           int dimensionWidth,
                           QueryLoad queryLoad,
                           Integer queryBatchSize,
                           TrainingDevice trainingDevice,
                           Integer shortSequenceLength,
                           Integer mediumSequenceLength,
                           Integer longSequenceLength) {
        this.currency = currency;
        this.prediction = prediction;
        this.numRows = numRows;
        this.epochsTrained = epochsTrained;
        this.maxEpochs = maxEpochs;
        this.startingLoss = startingLoss;
        this.finalLoss = finalLoss;
        this.modelType = modelType;
        this.queryType = queryType;
        this.trainingStartTime = trainingStartTime;
        this.trainingEndTime = trainingEndTime;
        this.queryStartTime = queryStartTime;
        this.queryEndTime = queryEndTime;
        this.sequenceLength = sequenceLength;
        this.batchSize = batchSize;
        this.dimensionWidth = dimensionWidth;
        this.queryLoad = queryLoad;
        this.queryBatchSize = queryBatchSize;
        this.trainingDevice = trainingDevice;
        this.shortSequenceLength = shortSequenceLength;
        this.mediumSequenceLength = mediumSequenceLength;
        this.longSequenceLength = longSequenceLength;
    }

    public static TrainingSessionBuilder builder(PricePredictionLookup lookup) {
        return new TrainingSessionBuilder(lookup);
    }
}
